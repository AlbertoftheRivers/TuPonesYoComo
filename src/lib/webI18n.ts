export const WEB_LANGUAGE_KEY = "tuponesyocomo.web.locale";

export type SupportedWebLanguage = "es" | "ca" | "fr" | "en" | "pt";

export const WEB_LANGUAGE_OPTIONS: { code: SupportedWebLanguage; labelKey: string }[] = [
  { code: "es", labelKey: "langSpanish" },
  { code: "ca", labelKey: "langCatalan" },
  { code: "fr", labelKey: "langFrench" },
  { code: "en", labelKey: "langEnglish" },
  { code: "pt", labelKey: "langPortuguese" },
];

export function readStoredLanguage(): SupportedWebLanguage {
  try {
    const v = localStorage.getItem(WEB_LANGUAGE_KEY);
    if (v === "es" || v === "ca" || v === "fr" || v === "en" || v === "pt") return v;
  } catch {
    /* private mode */
  }
  return "es";
}

export function writeStoredLanguage(lang: SupportedWebLanguage): void {
  try {
    localStorage.setItem(WEB_LANGUAGE_KEY, lang);
  } catch {
    /* */
  }
}

