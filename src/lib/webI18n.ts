export function translateProteinLabel(
  lang: SupportedWebLanguage,
  proteinValue: string,
  fallbackLabel: string
): string {
  const key = `protein_${proteinValue}`;
  const dict = webTranslations[lang] ?? webTranslations.es;
  const fall = webTranslations.es;
  return dict[key] ?? fall[key] ?? fallbackLabel;
}
